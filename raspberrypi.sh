# Raspberry Pi 3 (Debian/Raspbian) Setup

sudo apt update

## Terminal Emulator
sudo apt install lxterminal

## File Manager
sudo apt install pcmanfm

sudo apt install libreoffice

## Editing
sudo apt install gimp

